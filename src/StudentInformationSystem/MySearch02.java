    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
            //Check whether the search textfield is empty and then display an error message(if empty)
            if( searcht.getText().equals(""))
               JOptionPane.showMessageDialog(null, "Please enter ID before searching !");
            //If search textfield is not empty, perform the search action.
            else{
                for( int r=0; r<2; r++)   //initializing row
                    for( int c=0; c<18; c++) //initializing column
                        jTable1.setValueAt("", r, c);
                
               //Read from the  textfile 'item'
               BufferedReader rdfile= new BufferedReader( new FileReader("C:\\Users\\Dell\\Documents\\NetBeansProjects\\StudentInformationSystem\\whole.txt"));
               String[] line= new String[18];
               String search, target;
                
                boolean same, found=false;

                int x=0, row=0;
                while( (line[x]= rdfile.readLine()) != null)//reading items.txt - asigning to array[] line
                    x++;
                rdfile.close();
                search= searcht.getText();

                //Check whether the item name matches an item in the text file 
                //and will display all its details in tbl1.
                for( int k=0; line[k] != null; k++){
                    same= false;
                    target="";
                    for( int j=0; j < search.length(); j++){
                        target+= line[k].charAt(j);
                    }
                    if( search.equals(target))
                        same=true;

                    if( same){
                        String[] temp= line[k].split("\t");
                        jTable1.setValueAt((1000+k+1), row, 0);
                        for( int i=1; i<18; i++)
                            jTable1.setValueAt(temp[i-1], row,i);
                        row++;
                        found=true;
                    }
               } 

                //Display error message if the user input does not match to any of the input.
                if( !found)
                    JOptionPane.showMessageDialog(null, "Student not found !", "Error!",JOptionPane.ERROR_MESSAGE);

                searcht.setText("");
            }
        }catch(IOException |StringIndexOutOfBoundsException siobeex){
        }
    }//GEN-LAST:event_jButton1ActionPerformed

