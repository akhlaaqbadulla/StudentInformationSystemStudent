    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try {
            //Display error message if no item has been input
            if (searcht.getText().equals(""))
            JOptionPane.showMessageDialog(null, "Please enter Student ID", "Error !", JOptionPane.ERROR_MESSAGE);
            else {
                BufferedReader rdfile = new BufferedReader(new FileReader("whole.txt"));

                String[] itemline = new String[100];
                String[] temp;
                String delete = searcht.getText();
                boolean found = false;

                int x = 0;
                while ((itemline[x] = rdfile.readLine()) != null) {
                    temp = itemline[x].split("\t");

                    if (delete.equals(temp[0])) {
                        x = x+0;
                        found = true;
                    } else
                    x++;

                }

                rdfile.close();

                PrintWriter wrfile = new PrintWriter(new FileWriter("whole.txt"));
                for (int j = 0; itemline[j] != null; j++)
                wrfile.println(itemline[j]);

               wrfile.close();

                if (!found)
                JOptionPane.showMessageDialog(null, "Non existant data ! ");
                else
                JOptionPane.showMessageDialog(null, "Successfully deleted !");

               searcht.setText("");

            }
        } catch (IOException e) {}
    }//GEN-LAST:event_jButton3ActionPerformed

